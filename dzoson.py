import turtle

# Set the turtle's speed to the maximum value
turtle.speed(0)

# Set the turtle's pen size to a thicker width
turtle.pensize(5)

# Move the turtle to the starting position for the head
turtle.penup()
turtle.goto(0, 200)
turtle.pendown()

# Draw the head
turtle.circle(50)

# Move the turtle to the starting position for the body
turtle.penup()
turtle.goto(0, 50)
turtle.pendown()

# Draw the body
turtle.forward(100)

# Move the turtle to the starting position for the left arm
turtle.penup()
turtle.goto(-50, 100)
turtle.pendown()

# Draw the left arm
turtle.forward(50)

# Move the turtle to the starting position for the right arm
turtle.penup()
turtle.goto(50, 100)
turtle.pendown()

# Draw the right arm
turtle.forward(50)

# Move the turtle to the starting position for the left leg
turtle.penup()
turtle.goto(-25, 0)
turtle.pendown()

# Draw the left leg
turtle.forward(50)

# Move the turtle to the starting position for the right leg
turtle.penup()
turtle.goto(25, 0)
turtle.pendown()

# Draw the right leg
turtle.forward(50)

# Keep the window open until it is closed
turtle.exitonclick()

