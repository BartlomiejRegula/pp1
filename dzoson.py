import turtle

# Ustawienia początkowe
turtle.speed(5)
turtle.penup()
turtle.setpos(-100, -50)
turtle.pendown()

# Narysuj tułów
turtle.color("black", "orange")
turtle.begin_fill()
turtle.circle(50, 180)
turtle.end_fill()

# Narysuj głowę
turtle.color("black", "white")
turtle.begin_fill()
turtle.circle(40)
turtle.end_fill()

# Narysuj oczy
turtle.setpos(-10, 60)
turtle.color("black")
turtle.dot()
turtle.setpos(10, 60)
turtle.dot()

# Narysuj nos
turtle.setpos(0, 40)
turtle.color("black")
turtle.dot(10)

# Narysuj uszy
turtle.penup()
turtle.setpos(-50, 90)
turtle.pendown()
turtle.setpos(-80, 60)
turtle.penup()
turtle.setpos(50, 90)
turtle.pendown()
turtle.setpos(80, 60)

# Narysuj ogon
turtle.penup()
turtle.setpos(0, -50)
turtle.pendown()
turtle.setpos(0, -80)
turtle.penup()
turtle.setpos(-20, -60)
turtle.pendown()
turtle.setpos(-40, -80)
turtle.penup()
turtle.setpos(20, -60)
turtle.pendown()
turtle.setpos(40, -80)

turtle.hideturtle()
turtle.done()


